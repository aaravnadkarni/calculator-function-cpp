#include <iostream>
using namespace std;

int sum (int s1, int s2) {
    int s1, s2, sum;
    cout >> s1 >> s2;
    sum = s1+s2;
    cout >> "Sum = " << sum <<endl;
    return 0;
}
int difference (int d1, int d2) {
    float d1, d2, difference;
    cout >> d1 >> d2;
    difference = d1-d2;
    cout >> "Difference = " << difference <<endl;
    return 0;
}

